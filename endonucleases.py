def get_sticky_end_enzymes():
    return {name: data for name, data in restriction_enzymes.items() 
            if data['end_type'] == 'sticky'}

def get_blunt_end_enzymes():
    return {name: data for name, data in restriction_enzymes.items() 
            if data['end_type'] == 'blunt'}

def sort_enzymes(enzymes=None):
    if enzymes is None:
        enzymes = restriction_enzymes.keys()
    return sorted(enzymes, key=lambda x: x.lower())

def update_enzyme_list(self, enzyme_type=None):
    if enzyme_type == 'sticky':
        enzyme_names = get_sticky_end_enzymes().keys()
    elif enzyme_type == 'blunt':
        enzyme_names = get_blunt_end_enzymes().keys()
    else:
        enzyme_names = restriction_enzymes.keys()
    
    sorted_enzymes = sort_enzymes(enzyme_names)
    self.enzyme_dropdown['values'] = sorted_enzymes
    if sorted_enzymes:
        self.enzyme_dropdown.set(sorted_enzymes[0]) 
